    private void btn_dangnhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_dangnhapActionPerformed
        String tk = txt_taikhoan.getText().trim();
        String mk = new String(txt_matkhau.getPassword());
        String mk_hash = MaHoa.encrypt_md5(mk);
        if (tk.trim().isEmpty() || mk.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đủ tài khoản và mật khẩu!");
            return;
        }

        // Tài khoản không hợp lệ
        if (!tk.matches("[a-zA-Z0-9_]+")) {
            JOptionPane.showMessageDialog(this, "Tài khoản không được chứa ký tự đặc biệt!");
            txt_taikhoan.setText("");
            txt_matkhau.setText("");
            txt_taikhoan.requestFocus();
            return;
        }
        TaiKhoan taikhoan = ctrl_tk.login_NV(tk, mk_hash);

        if (taikhoan != null) {
            int quyen = taikhoan.getQuyen();
            String vaitro = (quyen == 0) ? "Nhân viên" : "Khách hàng";
            JOptionPane.showMessageDialog(this, 
                    "Đăng nhập thành công !"+"\nVai trò : "+vaitro);            
            // Đóng form đăng nhập và mở form chính với quyền tương ứng
            new Form_zMain(quyen).setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Tài khoản không đúng !");
            txt_taikhoan.setText("");
            txt_matkhau.setText("");
            txt_taikhoan.requestFocus();
        }
    }//GEN-LAST:event_btn_dangnhapActionPerformed

