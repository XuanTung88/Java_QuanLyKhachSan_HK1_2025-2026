    public boolean isValidUsername(String tk) {
        // 5-50 ký tự, chỉ chữ hoa, chữ thường và số
        String regex = "^[a-zA-Z0-9]{5,50}$";
        return tk.matches(regex);
    }
    public boolean isValidPassword(String mk) {
        // 5-100 ký tự, ít nhất 1 chữ hoa, 1 chữ thường, 1 ký tự đặc biệt
        String regex = "^(?=.*[a-z])(?=.*[A-Z])(?=.*[^a-zA-Z0-9]).{5,100}$";
        return mk.matches(regex);
    }


    private void txt_tieptucActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_tieptucActionPerformed
        String tk = txt_taikhoan.getText().trim();
        String mk = new String(txt_matkhau.getPassword());
        String remk = new String(txt_nhaplaimatkhau.getPassword());
        String hashmk = MaHoa.encrypt_md5(mk);
        if (tk.isEmpty() || mk.isEmpty() || remk.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!");
            return;
        }
        
        if (!mk.equals(remk)) {
            JOptionPane.showMessageDialog(this, "Mật khẩu nhập lại không khớp!");
            return;
        }
        
        if(tk.equals(mk)){
            JOptionPane.showMessageDialog(this, "Tài khoản và mật khẩu không được trùng nhau!");
            return;
        }
        
        if (service.is_Exist(tk)) {
            JOptionPane.showMessageDialog(this, "Tài khoản đã tồn tại!"+
                    "\nVui lòng chọn Tài khoản khác");
            return;
        }
        if (!isValidUsername(tk)) {
            JOptionPane.showMessageDialog(this, "Tài khoản không hợp lệ! "
                    + "(5-50 ký tự, chỉ chữ và số)"); 
            return;
        }

        if (!isValidPassword(mk)) {
            JOptionPane.showMessageDialog(this,  "Mật khẩu không hợp lệ! "
                    + "(5-100 ký tự, có chữ hoa, chữ thường, ký tự đặc biệt)");
            return;
        }

        boolean kq = service.register_KH(tk, hashmk);
        
        if(kq){
            JOptionPane.showMessageDialog(this, 
                    "Đăng ký khách hàng thành công!"+"\nHãy đăng nhập!");
            new Form_DangNhap().setVisible(true);
            this.dispose();
        }else {
        JOptionPane.showMessageDialog(this, "Đăng ký thất bại! ");
        }
    }//GEN-LAST:event_txt_tieptucActionPerformed

