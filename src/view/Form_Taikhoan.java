    void hienthi()
    {
        //System.out.println(Session.getGmail());
        kh=ctrKh.getKhachHangByEmail(Session.gmail);  
        if(kh==null)
        {
            tfGmail.setText(Session.getGmail());
            tfGmail.setEnabled(false);
            tfSdt.setText("");
            tfKhachHang.setText("");
            tfCccd.setText("");
            tfDiaChi.setText("");
        }
        else
        {
            tfGmail.setText(kh.getEmail());
            tfSdt.setText(kh.getSoDT());
            tfKhachHang.setText(kh.getTenKH());
            tfCccd.setText(kh.getCccd());
            tfDiaChi.setText(kh.getDiaChi());
        }
    }
