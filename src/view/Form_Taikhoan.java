    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        try{
            String gm=tfGmail.getText().trim();
            String ten=tfKhachHang.getText().trim();
            String cccd=tfCccd.getText().trim();
            String dc=tfDiaChi.getText().trim();
            String sdt=tfSdt.getText().trim();
            if(!Validation.isValidEmail(gm))
            {
                JOptionPane.showMessageDialog(this,"Gmail không đúng định dạng!");
                return;
            }

            if("".equals(gm) || "".equals(ten) || "".equals(cccd) || "".equals(dc) || "".equals(sdt))
            {
                JOptionPane.showMessageDialog(this,"Vùi lòng điền đầy đủ thông tin!");
                return;
            }
            if(ctrTk.is_Exist(gm)==true)
            {
                JOptionPane.showMessageDialog(this,"Gmail đã tồn tại!");
                return;
            }
            if(kh==null) kh=new KhachHang();
            kh.setTenKH(ten);
            kh.setEmail(gm);
            kh.setCccd(cccd);
            kh.setSoDT(sdt);
            kh.setDiaChi(dc);
            boolean kta=ctrKh.updateKhachHang(kh);
            boolean ktra=ctrTk.updateTaiKhoan(Session.getGmail(), gm);
            //System.out.println(Session.getGmail());
            if(kta==true && ktra==true) 
            {
                JOptionPane.showMessageDialog(this,"Cập nhật thành công. Vui lòng đăng nhập lại với đúng gmail đã thay đổi.");
                System.exit(0);

            }
            else JOptionPane.showMessageDialog(this,"Cập nhật không thành công");
        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, "Error : " + ex.getMessage());
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed


